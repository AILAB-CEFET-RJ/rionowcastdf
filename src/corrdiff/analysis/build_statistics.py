import pyarrow as pa
import pyarrow.parquet as pq


# =============================================================================
# PARQUET WRITER
# =============================================================================


class SummaryWriter:
    """
    Incrementally writes summary statistics to Parquet.

    The dataframe never stays completely in memory.
    """

    def __init__(self, output_file: Path):

        self.output_file = output_file

        self.writer = None

    def append(self, rows):

        if len(rows) == 0:
            return

        df = pd.DataFrame(rows)

        table = pa.Table.from_pandas(df)

        if self.writer is None:

            self.writer = pq.ParquetWriter(
                self.output_file,
                table.schema,
                compression="zstd",
            )

        self.writer.write_table(table)

    def close(self):

        if self.writer is not None:
            self.writer.close()


# =============================================================================
# UPDATE ACCUMULATORS
# =============================================================================


def update_accumulators(
    accumulator: StatisticsAccumulator,
    result: dict,
):

    """
    Update every global accumulator from one processed image.
    """

    accumulator.total_images += 1

    # ------------------------------------------------------------
    # dataframe row
    # ------------------------------------------------------------

    summary = result["summary"]

    accumulator.summary_buffer.append(summary)

    # ------------------------------------------------------------
    # spatial maps
    # ------------------------------------------------------------

    accumulator.grid_sum += result["grid_sum"]

    accumulator.grid_sq_sum += result["grid_sq_sum"]

    accumulator.grid_accum += result["grid_accum"]

    accumulator.grid_occurrence += result["grid_occurrence"]

    accumulator.valid_count += result["valid"]

    accumulator.grid_max = np.maximum(
        accumulator.grid_max,
        result["grid_max"],
    )

    # ------------------------------------------------------------
    # global statistics
    # ------------------------------------------------------------

    values = result["grid_accum"]

    mask = result["valid"].astype(bool)

    valid_values = values[mask]

    accumulator.global_sum += valid_values.sum()

    accumulator.global_sq_sum += (valid_values ** 2).sum()

    accumulator.global_pixels += valid_values.size

    if valid_values.size > 0:

        accumulator.global_max = max(

            accumulator.global_max,

            float(valid_values.max()),

        )

    # ------------------------------------------------------------
    # extreme event catalogue
    # ------------------------------------------------------------

    accumulator.extreme_events.append(

        {

            "timestamp": summary["timestamp"],

            "max_reflectivity": summary["maximum"],

            "rain_pixels": summary["rain_pixels"],

            "rain_fraction": summary["rain_fraction"],

            "volume": summary["volume"],

            "mean": summary["mean"],

        }

    )


# =============================================================================
# FLUSH SUMMARY BUFFER
# =============================================================================


def flush_summary_buffer(
    accumulator: StatisticsAccumulator,
    writer: SummaryWriter,
):

    """
    Writes buffered rows to parquet.
    """

    if len(accumulator.summary_buffer) == 0:
        return

    writer.append(accumulator.summary_buffer)

    accumulator.summary_buffer.clear()


# =============================================================================
# MAIN PROCESSING LOOP
# =============================================================================


def process_cache():

    """
    Main processing routine.

    Returns
    -------
    StatisticsAccumulator
    """

    OUTPUT_DIR.mkdir(
        exist_ok=True,
        parents=True,
    )

    logger.info("Scanning cache directory...")

    cache_files = discover_cache_files(
        Path(RADAR_CACHE_DIR)
    )

    logger.info(
    f"Radar cache files found: {len(cache_files):,}"
)

    accumulator = StatisticsAccumulator()

    writer = SummaryWriter(
        SUMMARY_PARQUET,
    )

    logger.info(
        "Starting parallel processing..."
    )

    with ProcessPoolExecutor(

        max_workers=MAX_WORKERS,

        initializer=initialize_worker,

    ) as executor:

        iterator = executor.map(

            process_file,

            cache_files,

            chunksize=250,

        )

        for result in tqdm(

            iterator,

            total=len(cache_files),

            desc="Processing radar cache",

        ):

            if result is None:

                accumulator.failed_images += 1

                continue

            update_accumulators(
                accumulator,
                result,
            )

            if (

                len(
                    accumulator.summary_buffer
                )

                >= PARQUET_BATCH_SIZE

            ):

                flush_summary_buffer(
                    accumulator,
                    writer,
                )

    logger.info(
        "Writing remaining rows..."
    )

    flush_summary_buffer(
        accumulator,
        writer,
    )

    writer.close()

    logger.info("Summary parquet completed.")

    logger.info(
        "Images processed : %d",
        accumulator.total_images,
    )

    logger.info(
        "Images failed    : %d",
        accumulator.failed_images,
    )

    return accumulator

# =============================================================================
# PART 3
#
# Spatial climatology generation
#
# This stage converts the accumulators into climatological products that are
# later used by the exploratory notebooks.
#
# Generated products
#
#   mean_grid.npy
#   std_grid.npy
#   max_grid.npy
#   accumulated_rain.npy
#   rain_occurrence.npy
#   rain_probability.npy
#
# =============================================================================


def generate_spatial_products(
    accumulator: StatisticsAccumulator,
):
    """
    Generates all spatial climatology products.

    Parameters
    ----------
    accumulator : StatisticsAccumulator

    Returns
    -------
    dict
        Dictionary containing every generated map.
    """

    logger.info("Generating spatial climatology maps...")

    valid = accumulator.valid_count > 0

    mean_grid = np.zeros_like(
        accumulator.grid_sum,
        dtype=np.float32,
    )

    std_grid = np.zeros_like(
        accumulator.grid_sum,
        dtype=np.float32,
    )

    rain_probability = np.zeros_like(
        accumulator.grid_sum,
        dtype=np.float32,
    )

    #
    # ------------------------------------------------------------------
    # Mean
    # ------------------------------------------------------------------
    #

    mean_grid[valid] = (

        accumulator.grid_sum[valid]

        /

        accumulator.valid_count[valid]

    )

    #
    # ------------------------------------------------------------------
    # Variance
    # ------------------------------------------------------------------
    #

    variance = np.zeros_like(
        accumulator.grid_sum,
        dtype=np.float64,
    )

    variance[valid] = (

        accumulator.grid_sq_sum[valid]

        /

        accumulator.valid_count[valid]

    ) - (

        mean_grid[valid] ** 2

    )

    variance = np.maximum(
        variance,
        0,
    )

    std_grid[valid] = np.sqrt(
        variance[valid]
    )

    #
    # ------------------------------------------------------------------
    # Rain probability
    # ------------------------------------------------------------------
    #

    rain_probability[valid] = (

        accumulator.grid_occurrence[valid]

        /

        accumulator.valid_count[valid]

    )

    #
    # ------------------------------------------------------------------
    # Max grid
    # ------------------------------------------------------------------
    #

    max_grid = accumulator.grid_max.astype(
        np.float32
    )

    #
    # ------------------------------------------------------------------
    # Accumulated rainfall
    # ------------------------------------------------------------------
    #

    accumulated_grid = accumulator.grid_accum.astype(
        np.float32
    )

    #
    # ------------------------------------------------------------------
    # Rain occurrence
    # ------------------------------------------------------------------
    #

    occurrence_grid = accumulator.grid_occurrence.astype(
        np.uint32
    )

    #
    # ------------------------------------------------------------------
    # Save every product
    # ------------------------------------------------------------------
    #

    logger.info("Saving mean grid...")

    np.save(
        MAP_MEAN,
        mean_grid,
    )

    logger.info("Saving std grid...")

    np.save(
        MAP_STD,
        std_grid,
    )

    logger.info("Saving max grid...")

    np.save(
        MAP_MAX,
        max_grid,
    )

    logger.info("Saving accumulated rainfall grid...")

    np.save(
        MAP_ACCUM,
        accumulated_grid,
    )

    logger.info("Saving rain occurrence grid...")

    np.save(
        MAP_OCCURRENCE,
        occurrence_grid,
    )

    logger.info("Saving rain probability grid...")

    np.save(
        MAP_PROBABILITY,
        rain_probability,
    )

    logger.info("Spatial products successfully generated.")

    return {

        "mean": mean_grid,

        "std": std_grid,

        "max": max_grid,

        "accumulated": accumulated_grid,

        "occurrence": occurrence_grid,

        "probability": rain_probability,

    }



# GLOBAL ACCUMULATORS
# =============================================================================


class StatisticsAccumulator:
    """
    Holds every accumulator required to build the climatological products.

    These arrays remain in memory during the entire execution.

    Memory footprint is negligible:

        70 x 84 x float64
    """

    def __init__(self):

        shape = (GRID_HEIGHT, GRID_WIDTH)

        self.grid_sum = np.zeros(shape, dtype=np.float64)

        self.grid_sq_sum = np.zeros(shape, dtype=np.float64)

        self.grid_max = np.zeros(shape, dtype=np.float32)

        self.grid_occurrence = np.zeros(
            shape,
            dtype=np.uint32,
        )

        self.grid_accum = np.zeros(
            shape,
            dtype=np.float64,
        )

        self.valid_count = np.zeros(
            shape,
            dtype=np.uint32,
        )

        #
        # Global statistics
        #

        self.total_images = 0

        self.failed_images = 0

        self.global_sum = 0.0

        self.global_sq_sum = 0.0

        self.global_pixels = 0

        self.global_max = 0.0

        #
        # Summary dataframe buffer.
        #

        self.summary_buffer = []

        #
        # Extreme events.
        #

        self.extreme_events = []
        

# =============================================================================
# GLOBAL STATISTICS
# =============================================================================


def generate_global_statistics(
    accumulator: StatisticsAccumulator,
):

    logger.info("Generating global statistics...")

    mean = (

        accumulator.global_sum

        /

        accumulator.global_pixels

    )

    variance = (

        accumulator.global_sq_sum

        /

        accumulator.global_pixels

    ) - mean ** 2

    variance = max(
        variance,
        0,
    )

    std = np.sqrt(variance)

    global_stats = {

        "total_images": int(
            accumulator.total_images
        ),

        "failed_images": int(
            accumulator.failed_images
        ),

        "valid_pixels": int(
            accumulator.global_pixels
        ),

        "global_mean": float(mean),

        "global_std": float(std),

        "global_max": float(
            accumulator.global_max
        ),

    }

    with open(
        GLOBAL_JSON,
        "w",
    ) as f:

        json.dump(
            global_stats,
            f,
            indent=4,
        )

    logger.info(
        "Global statistics written to %s",
        GLOBAL_JSON,
    )

    return global_stats


# =============================================================================
# EXTREME EVENT CATALOG
# =============================================================================


def build_extreme_event_catalog(
    accumulator: StatisticsAccumulator,
):

    logger.info(
        "Generating extreme event catalog..."
    )

    df = pd.DataFrame(
        accumulator.extreme_events
    )

    #
    # Composite severity score.
    #
    # The weights may be tuned later.
    #

    df["event_rank"] = (

        0.50 * df["max_reflectivity"]

        +

        0.30 * df["rain_fraction"]

        +

        0.20 * np.log1p(df["volume"])

    )

    df = df.sort_values(

        "event_rank",

        ascending=False,

    )

    df.reset_index(
        drop=True,
        inplace=True,
    )

    df.to_parquet(

        EXTREME_EVENTS,

        compression="zstd",

        index=False,

    )

    logger.info(

        "Extreme event catalog written."

    )

    logger.info(

        "Top event score : %.2f",

        df.iloc[0]["event_rank"],

    )

    return df



#!/usr/bin/env python3
"""
===============================================================================
build_statistics.py (Part 1)
===============================================================================

This script computes descriptive statistics from the radar cache generated by
build_radar_cache.py.

Unlike the original PNG archive, the cache contains one NumPy array per radar
timestamp already projected onto the CorrDiff grid (70 x 84), allowing the
entire historical archive to be processed efficiently.

The complete pipeline is divided into four implementation stages.

Part 1 implements:

    • configuration
    • logging
    • radar cache discovery
    • worker initialization
    • worker processing
    • accumulator data structures

Later parts will implement:

    Part 2
        - parallel processing
        - parquet generation

    Part 3
        - spatial climatology maps

    Part 4
        - global statistics
        - extreme event catalogue
        - JSON export

Author:
    Vinicius Gonçalves dos Santos

===============================================================================
"""

from __future__ import annotations

import json
import logging
import multiprocessing as mp
import os
from concurrent.futures import ProcessPoolExecutor
from pathlib import Path

import numpy as np
import pandas as pd
from tqdm import tqdm

from src.config.paths import RADAR_CACHE_DIR

# =============================================================================
# CONFIGURATION
# =============================================================================

OUTPUT_DIR = Path("datasets/radar_statistics")

SUMMARY_PARQUET = OUTPUT_DIR / "summary.parquet"

EXTREME_EVENTS = OUTPUT_DIR / "extreme_events.parquet"

GLOBAL_JSON = OUTPUT_DIR / "global_statistics.json"

MAP_MEAN = OUTPUT_DIR / "mean_grid.npy"

MAP_STD = OUTPUT_DIR / "std_grid.npy"

MAP_MAX = OUTPUT_DIR / "max_grid.npy"

MAP_ACCUM = OUTPUT_DIR / "accumulated_rain.npy"

MAP_OCCURRENCE = OUTPUT_DIR / "rain_occurrence.npy"

MAP_PROBABILITY = OUTPUT_DIR / "rain_probability.npy"

# -----------------------------------------------------

GRID_HEIGHT = 70

GRID_WIDTH = 84

RAIN_THRESHOLD = 0.0

PARQUET_BATCH_SIZE = 5000

MAX_WORKERS = max(1, mp.cpu_count() - 2)

# =============================================================================
# LOGGER
# =============================================================================


def setup_logger():

    logger = logging.getLogger("RadarStatistics")

    if logger.handlers:
        return logger

    logger.setLevel(logging.INFO)

    handler = logging.StreamHandler()

    formatter = logging.Formatter(
        "[%(asctime)s] [%(levelname)s] %(message)s"
    )

    handler.setFormatter(formatter)

    logger.addHandler(handler)

    return logger


logger = setup_logger()

# =============================================================================
# CACHE DISCOVERY
# =============================================================================


def discover_cache_files(cache_dir: Path):

    """
    Returns every cached radar grid (.npy).

    Files are sorted chronologically because their filenames follow

        YYYYMMDD_HHMM.npy

    Returns
    -------
    list(Path)
    """

    files = sorted(cache_dir.rglob("*.npy"))

    logger.info(
        "Radar cache files found: %d",
        len(files),
    )

    return files


# =============================================================================
# TIMESTAMP PARSER
# =============================================================================


def filename_to_timestamp(cache_file):
    """
    Converts cache filename to pandas.Timestamp.

    Supported format:

        YYYYMMDD_HH_MM.npy

    Example:

        20241231_23_58.npy
    """

    stem = cache_file.stem

    return pd.to_datetime(
        stem,
        format="%Y%m%d_%H_%M",
    )


# =============================================================================
# GLOBAL WORKER STATE
# =============================================================================

WORKER_CACHE = None

# =============================================================================
# WORKER INITIALIZER
# =============================================================================


def initialize_worker():

    """
    Executed exactly once for each worker process.

    Nothing heavy is loaded here because each worker simply reads .npy files.
    """

    global WORKER_CACHE

    WORKER_CACHE = True


# =============================================================================
# WORKER FUNCTION
# =============================================================================


def process_file(cache_file: Path):

    """
    Processes one cached radar image.

    Returns a lightweight dictionary containing all statistics required by the
    main process.

    Notes
    -----
    Spatial climatology maps are also accumulated here in order to minimize
    repeated passes over the dataset.
    """

    try:

        grid = np.load(cache_file)

    except Exception:

        return None

    valid = np.isfinite(grid)

    rain = valid & (grid > RAIN_THRESHOLD)

    rain_values = grid[rain]

    timestamp = filename_to_timestamp(cache_file)

    if rain_values.size == 0:

        summary = dict(

            timestamp=timestamp,

            rain_pixels=0,

            rain_fraction=0.0,

            mean=0.0,

            std=0.0,

            maximum=0.0,

            volume=0.0,

            p50=0.0,

            p90=0.0,

            p95=0.0,

            p99=0.0,
        )

    else:

        summary = dict(

            timestamp=timestamp,

            rain_pixels=int(rain.sum()),

            rain_fraction=float(rain.mean()),

            mean=float(rain_values.mean()),

            std=float(rain_values.std()),

            maximum=float(rain_values.max()),

            volume=float(rain_values.sum()),

            p50=float(np.percentile(rain_values, 50)),

            p90=float(np.percentile(rain_values, 90)),

            p95=float(np.percentile(rain_values, 95)),

            p99=float(np.percentile(rain_values, 99)),
        )

    #
    # Everything below is required later to build
    # climatological spatial maps.
    #

    return {

        "summary": summary,

        "grid_sum": np.nan_to_num(grid),

        "grid_sq_sum": np.nan_to_num(grid ** 2),

        "grid_max": np.nan_to_num(grid),

        "grid_occurrence": rain.astype(np.uint8),

        "grid_accum": np.nan_to_num(grid),

        "valid": valid.astype(np.uint8),
    }


